<h2>Introduction</h2>
<p>This web page shows how to repair a leaking heating element of an Ascaso Steel Uno Pro PID boiler (late 2011 production date). As this espresso machine was about five and a half years old and started leaking on the top of the boiler (noticeable from the outside by a hissing sound), it was decided to try to repair this at home instead of handing it over to a repair shop. In the end, the repair is fairly easy for the tech-savvy. This web-page is intended to show how it's done, and keep your espresso machine up-and running.</p>

