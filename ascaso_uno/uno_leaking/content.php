<h2>Summary</h2>
<p>The tools needed are common, see picture below: a 10, 13, and 17mm wrenche, and a long and short 3mm hex key. The latter is needed to loosen the boiler flange screws at the back of the flange. The boiler can be removed fairly easy by loosening the front switch cover. The electrical wiring does not have to be disconnected from the front panel, but tie-warps fixing the wiring may need to be cut. The operation takes less than two hours, from start to finish. According to the sparepart supplier, Ascaso changed material of the heating element packaging, which are now said to be Viton seals (red in color). For the fittings connecting the water inlet and steam outlet to the boiler, teflon (PTFE) tape was used during reassembly, although thread lock glue was adviced (but not available in hardware stores). These fittings appear to be tapered threads towards the boiler. Usefull to know: this repair was done within two hours time without prior knowledge of the espresso machine. So labour cost can be estimated when this repair is handed over to a repair facility. The risks are however that the heating element is damaged during removal when the nuts are too tight, or that there is heavy scale buildup. In that case, or as a precautionary measure, a new boiler can be installed instead.

