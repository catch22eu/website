        <h2>How to Design, Build and Program a Robot</h2>This robot
        project describes another approach to the common hexapod robot
        design. The project is divided in easy steps to explain the
        robot building process from a builder&#39;s perspective. The
        project starts by investigating the types of walking robots
        found on the Internet, and giving an overview of the available
        robot parts. The investigation results in a <a href=
        "http://catch22.eu/robotproject/design/">simple robot
        design</a> using r/c servos controlled by a programmable micro
        controller. Next, <a href=
        "http://catch22.eu/robotproject/building/">robot building</a>
        is explained by describing the tools needed and the way to use
        them. Finally, <a href=
        "http://catch22.eu/robotproject/programming/">robot
        programming</a> is described, which makes the robot walk around
        autonomously.<br />
