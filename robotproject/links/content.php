        <h3>Links</h3>

        <p>Following links are used for reference to buy parts,
        investigate kinds of robots, and nice sites for fun.</p>

        <h3>Robot Building Parts</h3>

        <p><a href=
        "http://www.graupner.com">http://www.graupner.com</a> and
        <a href=
        "http://www.servocity.com">http://www.servocity.com</a>: Show
        all kinds of servos, and has information about servo
        specifications and types of servos.<br />
        <a href="http://www.ksmetals.com">http://www.ksmetals.com</a>:
        Building materials used for the robot. You can not find what
        they sell in local hobby shops however. Graupner, mentioned
        above, does however show on-site what kind of metal building
        materials they offer.<br />
        <a href="http://www.gumstix.com">http://www.gumstix.com</a>:
        Very interesting micro computer which might be purchased in a
        later phase to give the robot much more intelligence when the
        software in the Brainstem is getting tool large.<br />
        <a href="http://www.hitecrcd.com/" rel=
        "nofollow">http://www.hitecrcd.com</a>: Like Graupner, they
        sell servos, and on this site you can find specs of them.<br />
        <a href="http://www.acroname.com">http://www.acroname.com</a>:
        The controller used in the robot.<br />
        <a href="http://www.conrad.nl">http://www.conrad.nl</a>:
        Excellent Dutch hobby -and electronics shop selling everything
        you need (apart from controllers) to build your robot.</p>

        <h3>Robot Examples</h3>

        <p><a href="http://www.tarry.de">http://www.tarry.de</a>: Very
        clean, high-tech design hexapod.<br />
        <a href=
        "http://www.lynxmotion.com">http://www.lynxmotion.com</a>:
        Shows several complete robot kits. The website also has
        interesting videos of the robots they sell, and shows the
        hexapod design video where you can see the sliding legs.<br />
        <a href=
        "http://home.earthlink.net/%7Ehenryarnold">http://home.earthlink.net/~henryarnold</a>:
        Nice design with the same concept as used above.<br />
        <a href=
        "http://home.ctlnet.com/%7Erobotguy67/hexapod/hexapod.htm" rel=
        "nofollow">home.ctlnet.com/~robotguy67/hexapod/hexapod.htm</a>:
        Shows excellent examples of high tech, home built robots. The
        site is interesting as well to gain knowledge on robot building
        from a builders&#39; perspective.<br />
        <a href=
        "http://www.beam-online.com">http://www.beam-online.com</a>:
        Uses 5 servos for walking. It shows an example of how to use
        servos directly as hinges, and how to attach legs to
        them.<br />
        <a href="http://bots_by_jbs.solarbotics.net/walker.html" rel=
        "nofollow">http://bots_by_jbs.solarbotics.net/walker.html</a>:
        The incredible walking machine. Uses solar power, and
        customized motors to propel itself.<br /></p>

