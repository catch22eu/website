			<h2>Introduction</h2>
			Since there's no official solution yet from Ultimaker, here's the catch22 solution for an UM 3 door. It is opened / closed by lifting ~5mm and pulling the bottom handle. The top handles have a grabbing mechanism and centers the door.  <br /><br />
			The model can be downloaded as a zip file (use at own risk, and non-commercial use): <a href="http://catch22.eu/3dprinting/u3door/um3_door.zip">here</a>. Feel free to comment on the <a href="https://ultimaker.com/en/community/39764-um3-door">Ultimaker Forum</a>!
			<br /><br />
			Note: Take care of the needed clearance of the print head, as well as the protruding print head if very large prints are printed (or custom prime positions are used), see the remark on the forum about this as well.<br /><br />
			Tricky part is finding the correct position to drill holes in the door itself (not too low, otherwise the extruder hits the clamps). There needs to be about 5mm play in z-direction between bottom clamps and top clamps, although the latches have slotted holes which aids well enough in drill hole accuracy. Best way to find the positions is to drill at least holes for the bottom and mount the bottom clamp, then find the correct position for the top clamps. The top clamps have sort of a centering mechanism, so both X and Z positions for the holes do matter. For door material it's suggested to take something that can withstand some temperatures (the example below is made with 4mm polysterene, which can handle 75C, although a bit thicker than 4mm is adviced to give it some more rigidity). The clamps are made for M4 size screws, and ABS of course. Take also care for additional remarks made on the forum when using maximum possible build volume.  			
