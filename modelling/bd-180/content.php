        <h2>Review BD-180 Airbrush</h2><img style=
        "width: 600px; height: 347px;" src="bd-180.jpg" alt="" /><br />

        <p>I picked up modelling again after 20 years or so, and
        started with just painting models with brushes. Final results
        were a bit disappoint by loosing detail by too thick paint.
        Therefore decided to buy a cheap airbrush and compressor, and
        see where this would lead to. I ended up with buying a (Fengda)
        Fenghua Bida Machinery Manufacture Co., Ltd. brush and
        compressor. Here&#39;s my experience so far - after I would say
        occasional use.</p><br />

        <h3>Airbrush Review</h3>

        <p>I bought the airbrush for about 30 Euro&#39;s. It has a
        0.2mm nozzle according to the SPECLFLCATIONS on the leaflet,
        but is advertised as a 0.25mm nozzle. It is a Chinese copy of
        Iwata&#39;s Custom Micro CM-C Plus.</p>

        <p>On the positive side</p>

        <ul>
          <li>
            <p>Cheap, good for novices who don&#39;t want to spend a
            fortune on equipment. Great way to learn how to use an
            airbrush. Most importantly, this means learn how to take
            care of it by adapting to a cleaning regime which does not
            block the airbrush with dried paint (see below).</p>
          </li>
        </ul>

        <p>On the negative side</p>

        <ul>
          <li>
            <p>The needle cap is too narrow. It interferes the paint
            spray a bit, which accumulates paint on the inside of the
            needle cap until being blown away as bigger droplets
            ruining your paint-job. I sanded off part of it, but in the
            end started to use the airbrush without the needle cap
            (although this has the risk of damaging the needle).</p>
          </li>

          <li>
            <p>The needle chucking guide has about 1mm play. When the
            needle retracts, this causes the needle to not only move
            backwards, but als a little downwards. This in turn causes
            the needle to be excentric in the nozzle when it
            progressively is pulled backwards. I did not notice this in
            an uneven spray pattern of the airbrush though, but things
            like this are typical for its quality.</p>
          </li>

          <li>
            <p>The previously described issue causes a bigger,
            noticeable problem with the handle (the back part of the
            airbrush with the cut-out). You&#39;re likely to touch the
            needle when you&#39;re spraying paint, causing much more
            paint to flow than intended. So pay attention to turn the
            handle in such a way that the cut-out is not positioned
            sideways (so basically not how it is shown in the
            picture).</p>
          </li>

          <li>
            <p>Broken nozzle threat and leaking air-valve. The
            air-valve was leaking air, always flowing a bit of air
            through the airbrush. Secondly, the threat broke off from
            the nozzle inside the bore of the airbrush while unscrewing
            the nozzle the 10<sup>th</sup> time or so. I returned my
            airbrush and got a new one which did not have a leaking
            air-valve. I guess I had a bad model first time</p>
          </li>

          <li>
            <p>The needle sometimes tends to stick in it&#39;s
            retracted position, which also causes paint droplets. It
            might be just my airbrush, but could well be manufacturing
            quality. I found out the spring tensioning cap (for the
            spring which presses the needle back into the nozzle), can
            be turned such a way that the needle jams inside the
            housing.</p>
          </li>
        </ul>

        <p>Wish-list for my next airbrush (if this one finally breaks
        down):</p>

        <ul>
          <li>
            <p>Better quality</p>
          </li>

          <li>
            <p>Wider needle cap</p>
          </li>

          <li>
            <p>Don&#39;t need a Micro Air Control Valve (that&#39;s the
            valve below the cup)</p>
          </li>

          <li>
            <p>Wider (&gt;0.3mm), threat-less nozzle</p>
          </li>

          <li>
            <p>Teflon seals?</p>
          </li>
        </ul>

        <p>So an option I&#39;m considering is Iwata&#39;s Revolution
        (HP-CR), which is entry level (around 90 Euro&#39;s), but uses
        a nozzle with threat, which is a weak point from my experience.
        Iwata&#39;s Eclipse (HP-CS, around 110 Euro&#39;s) is next step
        up, and seems to fit the bill.</p><br />

        <h3>Using the airbrush</h3>

        <ul>
          <li>
            <p>Safety first: use a respirator suited for spray
            painting. Airbrushes produce a very fine mist of foreign
            particles that do not belong in you lungs. I use a 3M 4251
            respirator.</p>
          </li>

          <li>
            <p>Clean, clean,clean! Extra handicap is that I use acrylic
            paints which dry fast, so also in my airbrush. At first I
            used to spray all paint I did not need anymore through the
            airbrush in a bottle, but this still produced a lot of
            paint cloud outside the bottle, and clogged my airbrush too
            soon. Nowadays I poor all the paint I don&#39;t need
            anymore away in another cup, and spray airbrush cleaner (I
            use Createx) through the airbrush on/into tissue paper
            until I see no paint anymore on the tissue paper and at the
            bottom of the paint cup where you can see the needle. Also,
            spraying longer than say 5 minutes or so lets the paint dry
            in the airbrush already, so I&#39;m now used to empty and
            clean the airbrush after about 5 minutes. When putting the
            airbrush away for a moment to mix paint, I leave a bit of
            cleaner inside the brush. Occasionally in between spray
            jobs I retract the needle, and use a q-tip drenched in
            alcohol and touch the tip of the nozzle, which seems to
            suck in a bit of alcohol by capillary action. The q-tip
            sometimes shows some paint left at the end of the nozzle,
            so it seems to help the nozzle stay clean. At the end of
            the spray sessions (and sometimes in between), I remove the
            needle, clean it with airbrush cleaner or alcohol. I remove
            the nozzle, use a small brush with cleaner or alcohol and
            clean it.</p>
          </li>

          <li>
            <p>When all is well, you can really notice you run out of
            paint by a distinct different sound from your airbrush. I
            sometimes suspect dust coming from tissue paper which clogs
            up the airbrush, and this can take several tries of
            cleaning until suddenly it starts spraying well again.
            I&#39;m still looking for a cheap lint-free cloth option
            though.</p>
          </li>

          <li>
            <p>Paint dilution. I use Vallejo Air paint, and found a lot
            of differences in paint thickness between bottles, but
            almost always I need to thin the paint for more than 50%,
            sometimes up to 100%. For thinning, I use about 50%
            distilled water, and 50% Vallejo&#39;s thinner. Strangely
            enough, it looks to me Vallejo&#39;s thinner itself is
            already too thick to be used in an airbrush. Recommendable
            is Geoff Illsley&#39;s / Compendium Series DVD about
            painting figures. This clearly explains how to mix paint
            correctly, and taking care of the airbrush.</p>
          </li>

          <li>
            <p>Pressure. About 1.5 bar. This seems to affect if the
            paint reaches the surface wet or dry, but also how much I
            dilute the paint.</p>
          </li>

          <li>
            <p>Last but not least, airbrush shops sell speedshapes
            (they look like computer mice outer shells, but a bit
            bigger), which are excellent to practice airbrushing and
            weathering painting skills.</p>
          </li>
        </ul>

        <p>I think that&#39;s about it. Enjoy.</p><br />
