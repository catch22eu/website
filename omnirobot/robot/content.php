	<h2>Introduction</h2>
	<p>This is my new project to build an autonomous omnidirectional robot. Main component Specs:</p>
	<ul>
		<li>A Beaglebone Green (wireless)</li>
		<li>3 Omni Wheels</li>
		<li>3 NEMA 14 Stepper Motors</li>
		<li>3 Pololu DRV8834 Stepper Drivers</li>
		<li>HC-SR04 Ultrasonic Rangefinders</li>
		<li>6600mAh LiPo Battery</li>
	</ul>
	<h2>First Prototype</h2>
